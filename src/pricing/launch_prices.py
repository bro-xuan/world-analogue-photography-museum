def lookup_launch_price(
    manufacturer_normalized: str, camera_name: str
) -> tuple[float, str, int] | None:
    """Look up the curated launch price for a camera.

    Returns (price, currency, year) or None if not found.
    Tries exact match first, then substring match on model_pattern.
    For substring matching, prefers longer (more specific) model_pattern matches.
    """
    camera_lower = camera_name.lower()

    # 1. Try exact match: (manufacturer, full camera name lowercase)
    exact_key = (manufacturer_normalized, camera_lower)
    if exact_key in LAUNCH_PRICES:
        return LAUNCH_PRICES[exact_key]

    # 2. Substring match: find all entries where model_pattern is in camera_lower
    candidates: list[tuple[int, tuple[float, str, int]]] = []
    for (mfr, pattern), price_info in LAUNCH_PRICES.items():
        if mfr == manufacturer_normalized and pattern in camera_lower:
            candidates.append((len(pattern), price_info))

    if not candidates:
        return None

    # Prefer the longest (most specific) pattern match
    candidates.sort(key=lambda x: x[0], reverse=True)
    return candidates[0][1]
