# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _clamp_cpi_year(year: int) -> int:
    """Clamp *year* to the CPI table range [1920, 2024]."""
    if year < 1920:
        return 1920
    if year > 2024:
        return 2024
    return year


def _nearest_year(table: dict[int, float], year: int) -> int:
    """Return the key in *table* closest to *year*."""
    if year in table:
        return year
    return min(table, key=lambda y: abs(y - year))


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def adjust_for_inflation(
    amount_usd: float,
    from_year: int,
    to_year: int = 2024,
) -> float:
    """Adjust a USD amount for inflation using CPI data.

    Formula::

        adjusted = original * (CPI_to_year / CPI_from_year)

    If *from_year* is before 1920, the 1920 CPI is used.
    If *to_year* is after 2024, the 2024 CPI is used.

    Returns the result rounded to 2 decimal places.
    """
    from_cpi = _CPI[_clamp_cpi_year(from_year)]
    to_cpi = _CPI[_clamp_cpi_year(to_year)]
    return round(amount_usd * (to_cpi / from_cpi), 2)


def convert_to_usd(
    amount: float,
    currency: str,
    year: int,
) -> float:
    """Convert a foreign-currency amount to USD using historical exchange rates.

    Supported currency codes: JPY, DEM, EUR, GBP, CNY, SEK, CZK, RUB, SUR.

    For German prices before 2002 use ``DEM``; from 2002 onward use ``EUR``.
    If the exact *year* is not in the table the nearest available year is used.

    Returns the result rounded to 2 decimal places.

    Raises ``ValueError`` for unsupported currency codes.
    """
    code = currency.upper()
    if code == "USD":
        return round(amount, 2)

    if code not in _EXCHANGE_RATES:
        raise ValueError(
            f"Unsupported currency code: {currency!r}. "
            f"Supported: {', '.join(sorted(_EXCHANGE_RATES))} and USD."
        )

    table = _EXCHANGE_RATES[code]
    lookup_year = _nearest_year(table, year)
    rate = table[lookup_year]  # foreign-currency units per 1 USD
    return round(amount / rate, 2)
